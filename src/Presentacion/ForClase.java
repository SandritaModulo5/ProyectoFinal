    private void bAddAtributoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddAtributoActionPerformed
        // TODO add your handling code here:
        if( !tNAtributo.getText().equals("") ){
            String nombre=tNAtributo.getText();
            String tipo=cbTipoAtributo.getSelectedItem().toString();
            
            int acceso=0;
            if( rbAPrivate.isSelected() )
               acceso=CONST.V_PRIVATE;
            if( rbAPublic.isSelected() )
               acceso=CONST.V_PUBLIC; 
            if( rbAProtected.isSelected() )
               acceso=CONST.V_PROTECTED; 
            
            Atributo atributo=new Atributo(nombre, tipo, acceso);
            
            listaA.add( atributo );            
            this.addAtributoLista( atributo );
            
            DefaultComboBoxModel modelo=(DefaultComboBoxModel)cbTipoAtributo.getModel();
            int index=modelo.getIndexOf( tipo );
            if( index<0 )
                modelo.addElement( tipo );
            cbTipoAtributo.setModel(modelo);                          
        }
        else
            JOptionPane.showMessageDialog( this, "Introdusca el nombre del Atributo", "Clase", JOptionPane.ERROR_MESSAGE);            
    }//GEN-LAST:event_bAddAtributoActionPerformed

