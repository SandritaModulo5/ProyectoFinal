    public void inicializar(){
        String nombre=this.clase.getNombre();        
        this.tNombre.setText( nombre );
        
        this.inicializarModeloAtributo( clase.obtenerLAtributos() );
        this.inicializarModeloMetodo( clase.obtenerLMetodos() );
    }
    
    public void inicializarModeloAtributo( LinkedList<Atributo> lista ){
        for (int i = 0; i < lista.size(); i++) {
            Atributo atributo = lista.get(i);
            modeloListaA.addElement( atributo.toStringGrafico() );
        }
        this.lAtributo.setModel( modeloListaA );
    }
    
    public void inicializarModeloMetodo( LinkedList<Metodo> lista ){
        for (int i = 0; i < lista.size(); i++) {
            Metodo metodo = lista.get(i);
            modeloListaM.addElement( metodo.toStringGrafico() );
        }
        this.lMetodo.setModel( modeloListaM );
    }    
    
   
    public void addAtributoLista( Atributo atributo ){
        modeloListaA.addElement( atributo.toStringGrafico() );
        this.lAtributo.setModel( modeloListaA );
    }
    
    public void addMetodoLista( Metodo metodo ){
        modeloListaM.addElement( metodo.toStringGrafico() );
        this.lMetodo.setModel( modeloListaM );
    }
    
   
    public void cargarMetodos( ){
        for (int i = 0; i < listaM.size(); i++) {
            Metodo metodo = listaM.get(i);
            this.clase.adicionarMetodo( metodo );
        }
        this.listaM=new LinkedList<Metodo>();
    }
    
    public void cargarAtributos( ){
        for (int i = 0; i < listaA.size(); i++) {
            Atributo atributo = listaA.get(i);
            this.clase.adicionarAtributo( atributo );
        }
        this.listaA=new LinkedList<Atributo>();
    }
    
    
    public void eliminarMetodos( ){
        for (int i = 0; i < listaMElim.size(); i++) {
            int index = Integer.parseInt( listaMElim.get(i) );
            this.clase.eliminarMetodo( index );                      
        }
        this.listaMElim=new LinkedList<String>();
    }
    
    public void eliminarAtributos( ){
        for (int i = 0; i < listaAElim.size(); i++) {
            int index = Integer.parseInt( listaAElim.get(i) );
            this.clase.eliminarAtributo( index );
        }
        this.listaAElim=new LinkedList<String>();
    }    
    
    private void ponerNombre(){
        if( !tNombre.getText().equals("") ){
            String nombre=tNombre.getText();
            this.clase.setNombre(nombre);
            
            if( rbAbstract.isSelected() )
                this.clase.getClase().setTipo( "abstract");
            if( rbFinal.isSelected() )
                this.clase.getClase().setTipo( "abstract");
            if( rbInterface.isSelected() )
                this.clase.getClase().setTipo( "interface");            
        }
        else
            JOptionPane.showMessageDialog( this, "Introdusca el nombre de la Clase", "Clase", JOptionPane.ERROR_MESSAGE); 
    }
    
